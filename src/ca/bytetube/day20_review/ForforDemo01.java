package ca.bytetube.day20_review;

public class ForforDemo01 {

    public static void main(String[] args) {

        for (int i = 0; i < 3; i++) {

            for ( int j = 0; j < 2; j++) {

                System.out.println("i:"+i+", j:"+j);

            }

            System.out.println("i: "+i);

        }
    }
}
